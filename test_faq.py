# テスト用のプロンプト
SYSTEM_PROMPT = "あなたは優秀な人事・法務アシスタントです。必ず提供された【ドキュメント】の内容のみに基づいて回答してください。ドキュメントに記載がない情報は絶対に推測せず、「記載がありません」とだけ回答してください。"

# テストケース
TEST_QUERIES = {
    "テスト１（基礎抽出）": "労働時間が7時間の場合、法律上最低何分の休憩を与える必要がありますか？",
    "テスト２（論理推論と例外処理）": "従業員全員に一斉に休憩を与えなくても良いのは、どのような条件を満たした場合ですか？",
    "テスト３（ハルシネーション防止）": "試用期間中の労働者であっても、残業代は通常の時給の何割増しで支払われますか？",
}

def run_tests():
    print("=== 客観的RAG（資料検索）モデル性能比較テストを開始します ===\n")

    for model in MODELS:
        print(f"▼▼▼ 実行モデル: {model} ▼▼▼")
        start_time = time.time()
        try:
            for test_name, query in TEST_QUERIES.items():
                print(f"\n 【{test_name}】: {query}")

                #コンテキストと質問を結合
                full_prompt = f"【ドキュメント】 \n{CONTEXT_TEXT}\n\n 【質問】 \n{query}"

                response = ollama.chat(model=model, messages=[
                    {'role': 'system', 'content': SYSTEM_PROMPT},
                    {'role': 'user', 'content': full_prompt},
                ])
                print(f"回答: {response['message']['content'].strip()}")
        
        except Exception as e:
            print(f"エラー発生（{model}）: {e}")
        
        elapsed_time = time.time() - start_time
        print(f"\n[処理時間: {elapsed_time:.2f}秒]")
        print("-" * 50)

if __name__ == "__main__":
    run_tests()

